using UnityEngine;

public class Billboard : MonoBehaviour
{
    //#############################################################################################################################
    //#############################################################################################################################
    //#############################################################################################################################
    //#############################################################################################################################
    //初期設定
    private Camera mainCamera => GameManager.MainCamera;

    private GameObject targetCharacter;

    //#############################################################################################################################
    //#############################################################################################################################
    //#############################################################################################################################
    //#############################################################################################################################
    private void Update()
    {
        //カメラの方を向く
        transform.LookAt(transform.position + mainCamera.transform.rotation * Vector3.forward, mainCamera.transform.up);
        // キャラクターに追従する
        if (targetCharacter)
        {
            transform.position = targetCharacter.transform.position + Vector3.up * 2.5f;
        }
    }

    // キャラクターをセットして追従させるメソッド
    public void FollowCharacter(GameObject character)
    {
        targetCharacter = character;
    }
    //#############################################################################################################################
    //#############################################################################################################################
    //#############################################################################################################################
    //#############################################################################################################################
}
